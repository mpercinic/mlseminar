import numpy as np
from matplotlib import pyplot as plt


#RMSE
x = np.array([10, 30, 50, 70, 90])
rf = np.array([4.57021673, 3.04837908, 2.65009104, 2.38956824, 2.16367953])
knn = np.array([3.68022404, 2.56964661, 2.28244918, 2.14296353, 1.96670108])
nm = np.array([0.01183137, 0.05297368, 0.05270496, 0.01325839, 0.013281])
lbfgsb = np.array([0.44385276, 0.4471294, 0.45293823, 0.45187543, 0.44910389])
slsqp = np.array([0.0079199, 0.00756956, 0.00746871, 0.00768199, 0.00806679])
cg = np.array([0.34057957, 0.34089412, 0.34068562, 0.34039615, 0.33116437])

plt.figure(figsize=(16, 10.87))
plt.rcParams.update({'font.size': 23})
plt.ylabel("RMSE", fontsize=28)
plt.xlabel("Training split size (%)", fontsize=28)
plt.xticks(x, labels=x)
#plt.plot(x, y_drnn_simple, 'blue', label="DRNN (simple)", alpha=0.7, marker="s")
#plt.fill_between(x, y_drnn_simple - stddev_drnn_simple, y_drnn_simple + stddev_drnn_simple, color='blue', alpha=0.2)
plt.plot(x, rf, 'red', label="RF", alpha=0.7, marker="o", markersize=8)
plt.plot(x, knn, 'blue', label="KNN", alpha=0.7, marker="^", markersize=8)
plt.plot(x, nm, 'green', label="NM", alpha=0.7, marker="D", markersize=8)
plt.plot(x, lbfgsb, 'purple', label="L-BFGS-B", alpha=0.7, marker="v", markersize=8)
plt.plot(x, slsqp, 'chocolate', label="SLSQP", alpha=0.7, marker="s", markersize=8)
plt.plot(x, cg, 'black', label="CG", alpha=0.7, marker="x", markersize=8)
plt.gca().set_ylim(bottom=0)
plt.grid(axis='y')
plt.legend(fontsize=22, ncol=5)
plt.savefig("rmse.png", dpi=200)
plt.show()

